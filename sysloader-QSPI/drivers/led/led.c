#include "usrinc.h"

////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////
//|          |
//| 函数名称 |: LED_Init
//| 功能描述 |:
//|          |:
//| 参数列表 |:
//|          |:
//| 返    回 |:
//|          |:
//| 备注信息 |:
//|          |:
////////////////////////////////////////////////////////////////////////////////
bool_t LED_Init(void)
{
  HW_SetPinFunc(9, 4, PIN_FUNC0);
  HW_GpioSetDir(9, 4, TRUE);
  HW_GpioClrVal(9, 4);  // 先点亮LED
  
  return TRUE;
}

////////////////////////////////////////////////////////////////////////////////
//|          |
//| 函数名称 |: LED_Toggle
